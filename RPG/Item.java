public class Item
{
    String name;
    String desc;
    int attack;
    int defense;
    int HRegen;
    int ERegen;
    int health;
    int energy;
    int bloodlust;
    
    public String getName(){
        return name;
    }
    
    public String getDescription(){
        return desc;
    }
    
    public int getAttack(){
        return attack;
    }

    public int getDefense(){
        return defense;
    }

    public int getHRegen(){
        return HRegen;
    }
    
    public int getERegen(){
        return ERegen;
    }
    
    public int getHealth(){
        return health;
    }
    
    public int getEnergy(){
        return energy;
    }
    
    public int getBloodlust(){
        return bloodlust;
    }
}
